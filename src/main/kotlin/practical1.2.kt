fun main(){
    var a = 5
    println("Integer Value:$a")
    var d=a.toDouble()
    println("Double value(from integer):$d")
    var e = "10"
    println("String value:$e")
    var t=e.toInt()
    println("integer value1(from string):$t")
    println("integer value2(from string):$t")
    var w=e.toDouble()
    println("double value(from string):$w")

}